import { IsString, IsOptional, IsEmail } from 'class-validator';

export class UpdateProfileDto {
  @IsString()
  @IsOptional()
  firstName?: string;

  @IsString()
  @IsOptional()
  lastName?: string;

  @IsEmail()
  @IsOptional()
  email?: string;

  @IsString()
  @IsOptional()
  phoneOrTelegram?: string;

  @IsString()
  @IsOptional()
  faculty?: string;

  @IsOptional()
  hasSeenWelcomeModal?: boolean;
}
